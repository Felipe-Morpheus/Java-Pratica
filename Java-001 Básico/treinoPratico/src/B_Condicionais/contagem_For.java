package B_Condicionais;

public class contagem_For {
	
	public static void main(String[] args) {
		//incrementando
		for (int i=0; i <5; i++) {
			System.out.println("Valor de i: " + i);
		}
		System.out.println(); //pulando linha
		
		
		//decrementando
		for ( int j=4; j>=0; j--) {
			System.out.println("Valor de j: " + j);
		}
		
	}

}
